package com.example.cmput301_team_project.enums;

/**
 * Enumeration representing the actions the user can perform on another user.
 *
 * FOLLOW   - request to follow the other user.
 * UNFOLLOW - unfollow the other user.
 * REMOVE   - remove the other user from the current user's followers list.
 */
public enum UserButtonActionEnum {
    FOLLOW,
    UNFOLLOW,
    REMOVE
}
